public class ExIf {
    public static void main(String[] args){

        int valor = 21;

        if(valor==7){
            System.out.println("Valor = 7");
        }
        else{
            if(valor > 7){
                System.out.println("Valor > 7");
            }
            else{
                System.out.println("Valor < 7");
            }

        }


    }
    
}
